""" Modil sa ladann genyen tout fonksyon kap pemet nus fe kalkil matematik. """

""" Fonksyon sa pemet nous fe adisyon. """
def fe_adisyon (nonb1 , nonb2):
    return nonb1 + nonb2

""" Fonksyon sa pemet nous fe soustraksyon. """
def fe_soustraksyon (nonb1 , nonb2):
    return nonb1 - nonb2

""" Fonksyon sa pemet nous fe miltiplikasyon. """
def fe_miltiplikasyon (nonb1 , nonb2):
    return nonb1 * nonb2

""" Fonksyon sa pemet nous fe divizyon. """
def fe_divizyon (nonb1 , nonb2):
    return nonb1 / nonb2